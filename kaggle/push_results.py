#!/usr/bin/env python3
"""
推送实验结果回 GitHub

在 Kaggle Notebook 中执行此脚本，将结果 commit & push 回 GitHub。
"""
import os
import sys
import subprocess
from pathlib import Path
from datetime import datetime

# 项目根目录
PROJECT_ROOT = Path(__file__).parent.parent

def run_cmd(cmd: str, cwd: Path = None):
    """运行命令"""
    print(f"$ {cmd}")
    result = subprocess.run(
        cmd,
        shell=True,
        cwd=cwd or PROJECT_ROOT,
        capture_output=True,
        text=True
    )
    if result.stdout:
        print(result.stdout)
    if result.stderr:
        print(f"stderr: {result.stderr}")
    return result.returncode == 0

def push_results(experiment_id: str):
    """推送结果回 GitHub"""
    print("="*60)
    print("推送实验结果回 GitHub")
    print("="*60)
    
    # 配置 git 避免交互式确认
    print("\n[0/5] 配置 git...")
    run_cmd("git config user.name \"Kaggle Bot\"")
    run_cmd("git config user.email \"kaggle@example.com\"")
    print("   ✓ git 配置完成")
    
    # 检查 git 状态
    print("\n[1/5] 检查 git 状态...")
    if not run_cmd("git status -s"):
        print("❌ git status 失败")
        return False
    
    # 添加结果文件
    print("\n[2/5] 添加结果文件...")
    results_path = f"kaggle/results/{experiment_id}"
    if not run_cmd(f"git add {results_path}"):
        print(f"❌ git add {results_path} 失败")
        return False
    
    # Commit
    print("\n[3/5] Commit...")
    commit_msg = f"Add experiment results: {experiment_id}\n\nAutomatically generated by Kaggle Notebook"
    if not run_cmd(f'git commit -m "{commit_msg}"'):
        print("❌ git commit 失败（可能没有新文件）")
        # 不返回 False，继续尝试
    
    # Push（ quiet 模式）
    print("\n[4/5] Push to GitHub...")
    if not run_cmd("git push origin main -q"):
        print("❌ git push 失败")
        return False
    
    print("\n[5/5] 完成！")
    print("="*60)
    print("✅ 结果已成功推送到 GitHub！")
    print("="*60)
    
    return True

if __name__ == "__main__":
    # 从命令行参数获取 experiment_id，或使用最新的
    if len(sys.argv) > 1:
        experiment_id = sys.argv[1]
    else:
        # 找最新的实验目录
        results_dir = PROJECT_ROOT / "kaggle" / "results"
        exp_dirs = sorted([d for d in results_dir.iterdir() if d.is_dir()], reverse=True)
        if exp_dirs:
            experiment_id = exp_dirs[0].name
        else:
            print("❌ 没有找到实验结果目录")
            sys.exit(1)
    
    print(f"使用实验ID: {experiment_id}")
    success = push_results(experiment_id)
    sys.exit(0 if success else 1)
